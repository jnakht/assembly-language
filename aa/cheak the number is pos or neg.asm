
.MODEL SMALL
.STACK 100H

.DATA
    MSG1 DB "ENTER A NUMBER: $"
    POSITIVE_MSG DB 10, 13, "THE NUMBER IS POSITIVE$"
    NEGATIVE_MSG DB 10, 13, "THE NUMBER IS NEGATIVE$"

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    LEA DX, MSG1
    MOV AH, 09H
    INT 21H
    MOV AH, 01H
    INT 21H 
    
    
    SUB AL, 30H     
    CBW             
    TEST AL, 80H  
    JS NEGATIVE     

    LEA DX, POSITIVE_MSG
    MOV AH, 09H
    INT 21H
    JMP EXIT

NEGATIVE:
    LEA DX, NEGATIVE_MSG
    MOV AH, 09H
    INT 21H

EXIT:
    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
